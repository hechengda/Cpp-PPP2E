#include "std_lib_facilities.h"

//------------------------------------------------------------------------------

int ga[] = {1, 2, 4, 8, 16, 32, 64, 128, 256, 512, 1024};

//------------------------------------------------------------------------------

void print_array(int a[], int n)
{
    for (int i = 0; i<n; ++i)
        cout<<a[i]<<' ';
    cout<<'\n';
}

//------------------------------------------------------------------------------

void f(int a[], int n)
{
    int la[10];
    copy(ga, ga+10, la);
    print_array(la, 10);

    int* p = new int[n];
    copy(a, a+n, p);
    print_array(p, 10);
    delete[] p;
}

//------------------------------------------------------------------------------

int fact(int x)
{
    int res = 1;
    while (x) {
        res *= x;
        --x;
    }
    return res;
}

//------------------------------------------------------------------------------

int main()
{
    f(ga, 10);

    int aa[10];
    for (int i = 0; i<10; ++i)
        aa[i] = fact(i+1);

    f(aa, 10);
}

